# multiply()" function that takes "num1" and "num2" as parameters
#            and return the product of "num1" and "num2"
def multiply(n1=1,n2=1):
    """ function to multiply 2 numbers"""
    n3 = n1*n2
    return n3
res = multiply()
print("The result is:",res)
res = multiply(10)
print("The result is:",res)
res = multiply(10,2)
print("The result is:",res)